#include "libft.h"

int		ft_sqrt(int nb)
{
	int		a;
	int		c;

	a = 1;
	c = 0;
	while (a <= nb)
	{
		c = a * a;
		if (c == nb)
			return (a);
		a++;
	}
	return (0);
}
