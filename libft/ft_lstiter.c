#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	t_list	*i;

	if (lst && f)
	{
		i = lst;
		while (i != NULL)
		{
			(*f)(i);
			i = i->next;
		}
	}
}
